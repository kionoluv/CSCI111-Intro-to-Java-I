package lab1_simplebusinesscard;

public class Lab1_SimpleBusinessCard {

    public static void main(String[] args) {
        
        // prints first, middle, and last name on the same line
        System.out.print("Ngoc-Nhi ");
        System.out.print("Victoria ");
        // prints last name and following string will be on a new line
        System.out.println("Pham");
        
        // prints major and following tring will be a new line
        System.out.println("Computer Science Major");
        
        // prints college and following string will be a new line
        System.out.println("Community College of Philadelphia");
        
        // prints building number and street name on the same line
        System.out.print("1700 ");
        // prints the street name and the following string will be on a new line
        System.out.println("Spring Garden Street");
        
        // prints city, state, and zip code on the same line
        System.out.print("Philadelphia, ");
        System.out.print("PA ");
        //prints zip code and following string will be on a new line
        System.out.println("19130");
        
        // prints email and following string will be on a new line
        System.out.println("npham15@ccp.edu");
    } // end main
    
} // end class Lab1_SimplebusinessCard
